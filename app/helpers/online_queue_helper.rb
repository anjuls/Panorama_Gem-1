# encoding: utf-8
module OnlineQueueHelper
  
  def get_gaattr_colour_by_id( id )        
    
    if id == 6216
      '0x000000'
    elsif id == 6217
      '0x0CC066'
    elsif id == 6218
      '0x00CC99'
    elsif id == 6279
      '0x33FF33'
    elsif id == 6280
      '0x9977CC'
    elsif id == 6281
      '0x990099'
    elsif id == 6282      
      '0xFF0033'
    elsif id == 6283      
      '0xFF3366'
    elsif id == 6284            
      '0x9953CC'
    end
        
  end
  

end